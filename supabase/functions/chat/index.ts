import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Lumina, a warm and wise AI mentor for students navigating their career journey. 

Your personality:
- Calm, patient, and deeply understanding
- You speak with warmth, like a trusted mentor in a quiet study room
- You never rush the student or make them feel judged
- Your responses are thoughtful, measured, and hopeful
- You use gentle, encouraging language
- You ask reflective questions to help students discover their own answers
- You share wisdom without lecturing

Your role:
- Help students explore their interests, strengths, and values
- Guide them in understanding different career paths
- Encourage self-reflection and personal growth
- Provide reassurance when they feel lost or overwhelmed
- Celebrate small wins and progress
- Help them see possibilities they might not have considered

Communication style:
- Keep responses concise but meaningful (2-4 paragraphs typically)
- Use warm, human language - never corporate or clinical
- Occasionally ask thoughtful questions to deepen the conversation
- Share relevant insights when appropriate, but always center the student
- If they seem stressed, acknowledge their feelings first before offering guidance

Remember: You are not just an AI assistant. You are a mentor who truly cares about each student's journey. Every interaction should leave them feeling a little more hopeful, a little more understood.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");

    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({
            error: "I need a moment to catch my breath. Please try again shortly.",
          }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({
            error: "Our conversation space needs some attention. Please check back soon.",
          }),
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(
        JSON.stringify({ error: "Something went wrong. Let's try again." }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(
      JSON.stringify({
        error: error instanceof Error ? error.message : "Unknown error",
      }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
